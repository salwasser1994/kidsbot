async def begin_quiz(callback: CallbackQuery):
    user_id = callback.from_user.id
    topic = callback.data.split(":", 1)[1]

    questions = all_questions.get(topic, []).copy()
    if not questions:
        await callback.message.edit_text("–í–æ–ø—Ä–æ—Å—ã –¥–ª—è —ç—Ç–æ–π —Ç–µ–º—ã –ø–æ–∫–∞ –Ω–µ –¥–æ–±–∞–≤–ª–µ–Ω—ã.", reply_markup=main_menu())
        return

    random.shuffle(questions)  # –ü–µ—Ä–µ–º–µ—à–∏–≤–∞–µ–º –≤–æ–ø—Ä–æ—Å—ã

    active_quiz[user_id] = {
        "question_index": 0,
        "questions": questions,
        "last_text": callback.message,
        "topic": topic
    }

    await send_quiz_question(user_id, callback.message.chat.id)


# === –û–¢–ü–†–ê–í–ö–ê –í–û–ü–†–û–°–ê ===
async def send_quiz_question(user_id, chat_id, result_text=""):
    quiz = active_quiz.get(user_id)
    if not quiz:
        return

    q_index = quiz["question_index"]

    # –ï—Å–ª–∏ –≤–æ–ø—Ä–æ—Å—ã –∑–∞–∫–æ–Ω—á–∏–ª–∏—Å—å
    if q_index >= len(quiz["questions"]):
        user_name = get_child(user_id)
        final_text = f"{quiz['topic']} –∑–∞–∫–æ–Ω—á–µ–Ω–∞! –¢–≤–æ–∏ –æ—á–∫–∏: {users[user_name]['points']}"
        try:
            await quiz["last_text"].edit_text(final_text, reply_markup=main_menu())
        except TelegramBadRequest:
            pass
        del active_quiz[user_id]
        return

    question, options, correct_answer = quiz["questions"][q_index]

    # –ü–µ—Ä–µ–º–µ—à–∏–≤–∞–µ–º –≤–∞—Ä–∏–∞–Ω—Ç—ã –æ—Ç–≤–µ—Ç–∞
    shuffled_options = options.copy()
    random.shuffle(shuffled_options)
    correct_index = shuffled_options.index(correct_answer)

    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –¥–∞–Ω–Ω—ã–µ
    quiz["shuffled_options"] = shuffled_options
    quiz["correct_index"] = correct_index

    kb = InlineKeyboardMarkup(
        inline_keyboard=[[InlineKeyboardButton(text=opt, callback_data=f"quiz_ans:{i}")] 
                         for i, opt in enumerate(shuffled_options)]
                     + [[InlineKeyboardButton(text="üè†", callback_data="main_menu")]]
    )

    user_name = get_child(user_id)
    points = users[user_name]["points"]
    text_parts = []
    if result_text:
        text_parts.append(result_text)
        text_parts.append(f"üèÜ –û—á–∫–∏: {points}")
    text_parts.append(f"–í–æ–ø—Ä–æ—Å {q_index + 1}: {question}")
    text = "\n".join(text_parts)

    try:
        await quiz["last_text"].edit_text(text, reply_markup=kb)
    except TelegramBadRequest as e:
        if "message is not modified" not in str(e):
            raise

@dp.callback_query(F.data.startswith("quiz_ans:"))
async def quiz_answer(callback: CallbackQuery):
    await callback.answer()

    user_id = callback.from_user.id
    if user_id not in active_quiz:
        await callback.message.answer("–í–∏–∫—Ç–æ—Ä–∏–Ω–∞ –Ω–µ –∞–∫—Ç–∏–≤–Ω–∞")
        return

    quiz = active_quiz[user_id]
    chosen_index = int(callback.data.split(":")[1])  # –∏–Ω–¥–µ–∫—Å –≤—ã–±—Ä–∞–Ω–Ω–æ–≥–æ –≤–∞—Ä–∏–∞–Ω—Ç–∞
    correct_index = quiz["correct_index"]

    user_name = get_child(user_id)
    if not user_name:
        await callback.message.answer("–¢—ã –Ω–µ –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω.")
        return

    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø—Ä–∞–≤–∏–ª—å–Ω–æ—Å—Ç—å –æ—Ç–≤–µ—Ç–∞
    if chosen_index == correct_index:
        users[user_name]["points"] += 1
        result_text = "‚úÖ –ü—Ä–∞–≤–∏–ª—å–Ω–æ!"
    else:
        users[user_name]["points"] = max(0, users[user_name]["points"] - 1)
        correct_answer = quiz["shuffled_options"][correct_index]
        result_text = f"‚ùå –ù–µ–ø—Ä–∞–≤–∏–ª—å–Ω–æ!"

    # –ü–µ—Ä–µ—Ö–æ–¥–∏–º –∫ —Å–ª–µ–¥—É—é—â–µ–º—É –≤–æ–ø—Ä–æ—Å—É
    quiz["question_index"] += 1
    await send_quiz_question(user_id, callback.message.chat.id, result_text)

