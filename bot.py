    events["vacation"] = date(today.year, 6, 1)

    # 23 —Ñ–µ–≤—Ä–∞–ª—è
    events["defender_day"] = date(today.year, 2, 23)

    # 8 –º–∞—Ä—Ç–∞
    events["women_day"] = date(today.year, 3, 8)

    # –ü–æ–ª—É—á–∞–µ–º –≤—ã–±—Ä–∞–Ω–Ω—É—é –¥–∞—Ç—É
