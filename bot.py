    user_id = callback.from_user.id
    user_name = get_child(user_id)
    choice = callback.data.split(":")[1]

    today = date.today()
    events = {}

    # –î–µ–Ω—å —Ä–æ–∂–¥–µ–Ω–∏—è
    if user_name:
        birthday_str = users[user_name]["birthday"]  # —Ñ–æ—Ä–º–∞—Ç "YYYY-MM-DD"
        birthday_dt = datetime.strptime(birthday_str, "%Y-%m-%d").date()
        # –ë–µ—Ä—ë–º —Ç–æ–ª—å–∫–æ –º–µ—Å—è—Ü –∏ –¥–µ–Ω—å, –≥–æ–¥ —Ç–µ–∫—É—â–∏–π
        birthday_this_year = date(today.year, birthday_dt.month, birthday_dt.day)
        events["birthday"] = birthday_this_year
    else:
        events["birthday"] = date(today.year, 1, 1)  # –Ω–∞ —Å–ª—É—á–∞–π –Ω–µ–∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω–Ω–æ–≥–æ

